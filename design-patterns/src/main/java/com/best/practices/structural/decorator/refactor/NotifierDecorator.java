package com.best.practices.structural.decorator.refactor;

public abstract class NotifierDecorator extends Notifier {
    private Notifier wrappedNotifier;

    public NotifierDecorator(Notifier wrappedNotifier) {
        this.wrappedNotifier = wrappedNotifier;
    }

    @Override
    public void send(String message) {
        wrappedNotifier.send(message);
    }
}